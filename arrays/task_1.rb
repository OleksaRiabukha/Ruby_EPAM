=begin
 Є масив з довільними числами. 
 Зробіть так, щоб елемент повторився в масиві таку кількість разів яка відповідає його числу.
  Приклад: [1, 3, 2, 4] перетворюється в [1, 3, 3, 3, 2, 2, 4, 4, 4, 4].
=end


def multiply_nums(arr)
    arr.map{|n| n.to_s * n}.join("").to_i.digits.reverse
end


p multiply_nums([1, 3, 2, 4])
p multiply_nums([3, 8, 4, 7])

